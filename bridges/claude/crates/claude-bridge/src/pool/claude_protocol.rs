// ============================================================================
// Pool-internal broadcast event
// ============================================================================
//
// `ClaudeEvent` is what the pool's `broadcast::Sender` publishes to all
// translator subscribers. For now it is a thin wrapper around `ClaudeOutbound`
// — the bridge does not need to enrich the wire payload before broadcasting.
// Kept as a separate type so future bookkeeping (e.g. arrival timestamps for
// debugging) can be added without breaking subscribers.

#[derive(Debug, Clone)]
pub struct ClaudeEvent {
    pub payload: ClaudeOutbound,
}

impl ClaudeEvent {
    pub fn new(payload: ClaudeOutbound) -> Self {
        Self { payload }
    }
}

