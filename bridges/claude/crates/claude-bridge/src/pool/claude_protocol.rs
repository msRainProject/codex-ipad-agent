//! Serde mirror of the `claude -p --output-format stream-json
//! --include-partial-messages --verbose` wire shapes.
//!
//! Source of truth (live probe against `~/.claude` v2.1.121, see plan
//! `~/.claude/plans/review-this-project-and-polymorphic-rabbit.md`):
//!
//! ```text
//! echo '{"type":"user",...}' | claude -p \
//!   --input-format stream-json \
//!   --output-format stream-json \
//!   --include-partial-messages \
//!   --verbose \
//!   --dangerously-skip-permissions \
//!   --max-turns 1 --model haiku
//! ```
//!
//! Every line claude writes to stdout is one of `system | stream_event |
//! assistant | user | result | rate_limit_event`, tagged on the `type`
//! discriminator. We deserialize the envelope into [`ClaudeOutbound`] and
//! pass the parsed payload up to the bridge translator. Fields the bridge
//! does not introspect today are kept as `serde_json::Value` so additive
//! claude releases don't require a schema change.
//!
//! Translator-facing event broadcasted by the pool is [`ClaudeEvent`], which
//! wraps `ClaudeOutbound` verbatim — this crate is responsible only for
//! parsing and routing; codex-shaped translation happens in
//! `claude_translate` (owned by the `claude-translate` teammate).

use std::collections::BTreeMap;

use serde::{Deserialize, Serialize};
use serde_json::Value;

// ============================================================================
// Inbound: bridge → claude on stdin
// ============================================================================
//
// `claude -p --input-format stream-json` accepts one JSON object per stdin
// line. The wire shape is the Anthropic SDK `MessageParam` family wrapped in
// `{ "type": "user", "message": { ... } }`. Claude only consumes user-role
// messages on stdin (assistant/tool messages happen out-of-band via tool
// results inside its own loop), so we model just that one inbound variant.

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
#[serde(tag = "type", rename_all = "snake_case")]
pub enum ClaudeInbound {
    User(ClaudeUserMessageEnvelope),
    /// Out-of-band control message that drives a sidecar protocol on stdin
    /// (interrupt, set_model, rewind_files, ...). claude replies with a
    /// matching `control_response{request_id}` envelope; the bridge correlates
    /// via `pool::process`'s pending-control-response table.
    ControlRequest(ControlRequestEnvelope),
    /// Cancel a previously-sent control_request. Useful when the bridge
    /// changes its mind mid-flight (e.g. user closes the dialog while a
    /// pending `can_use_tool` response is still being composed). v2 wiring
    /// only — none of the handler paths originate cancellations today.
    ControlCancelRequest(ControlCancelEnvelope),
    /// Bridge replying to claude's inbound `control_request{can_use_tool|...}`
    /// when the bridge is acting as the tool-permission server (i.e. spawned
    /// with `--permission-prompt-tool stdio`). Wire shape mirrors the inbound
    /// `control_response` envelope — `request_id` lives nested inside the
    /// outer `response` object, NOT at top level.
    ControlResponse(OutboundControlResponseEnvelope),
}

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct OutboundControlResponseEnvelope {
    pub response: OutboundControlResponseInner,
}

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
#[serde(tag = "subtype", rename_all = "snake_case")]
pub enum OutboundControlResponseInner {
    Success {
        request_id: String,
        /// Subtype-specific reply payload claude is waiting for. For
        /// `can_use_tool` this is `{behavior:"allow", updatedInput?, ...} |
        /// {behavior:"deny", message?, interrupt?, ...}`.
        #[serde(default, skip_serializing_if = "Option::is_none")]
        response: Option<Value>,
    },
    Error {
        request_id: String,
        error: String,
    },
}

/// Wire shape: `{"type":"control_request","request_id":"<uuid>","request":{...subtype...}}`.
/// `request_id` is generated by [`crate::pool::process::ClaudeProcessHandle::request_control`]
/// so callers don't need to manage correlation.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct ControlRequestEnvelope {
    pub request_id: String,
    pub request: ControlRequestBody,
}

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct ControlCancelEnvelope {
    pub request_id: String,
}

/// Typed mirror of the SDK's outbound control_request `request` payloads (see
/// `~/dev/cc/node_modules/@anthropic-ai/claude-agent-sdk/sdk.mjs`, search for
/// `subtype:"`). Variants the bridge actually originates today are spelled
/// out for type safety; everything else degrades to [`ControlRequestBody::Other`]
/// so future SDK features remain forwardable without a schema bump.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
#[serde(tag = "subtype", rename_all = "snake_case")]
pub enum ControlRequestBody {
    /// Aborts the in-flight turn. Claude replies with `subtype:"success"` or
    /// `subtype:"error"`. No request fields.
    Interrupt,
    /// Hot-swap the model for subsequent turns on the same process.
    SetModel { model: String },
    /// Toggle permission mode mid-thread (e.g. `default → bypassPermissions`).
    SetPermissionMode { mode: String },
    /// Cap the thinking budget in tokens.
    SetMaxThinkingTokens { tokens: u32 },
    /// Cancel a single subagent task by its task id.
    StopTask { task_id: String },
    /// Roll the on-disk transcript back to the supplied user message id.
    /// `dry_run:true` returns the would-be result without committing.
    RewindFiles {
        user_message_id: String,
        #[serde(default, skip_serializing_if = "Option::is_none")]
        dry_run: Option<bool>,
    },
    /// SDK's first-frame initializer that registers hooks/MCP/etc. The bridge
    /// does not send this in v1 (we rely on the user-message-triggers-init
    /// quirk), but the variant is provided so a v2 cleaner-init-path
    /// experiment can flip to it without touching this enum.
    Initialize {
        #[serde(default, skip_serializing_if = "Option::is_none")]
        hooks: Option<Value>,
        #[serde(
            default,
            rename = "sdkMcpServers",
            skip_serializing_if = "Option::is_none"
        )]
        sdk_mcp_servers: Option<Vec<String>>,
        #[serde(
            default,
            skip_serializing_if = "Option::is_none",
            rename = "jsonSchema"
        )]
        json_schema: Option<Value>,
        #[serde(
            default,
            skip_serializing_if = "Option::is_none",
            rename = "systemPrompt"
        )]
        system_prompt: Option<String>,
        #[serde(
            default,
            skip_serializing_if = "Option::is_none",
            rename = "appendSystemPrompt"
        )]
        append_system_prompt: Option<String>,
        #[serde(default, skip_serializing_if = "Option::is_none")]
        agents: Option<Value>,
    },
    /// Catch-all that carries the raw payload through. Used both for SDK
    /// subtypes the bridge doesn't model (mcp_*) and for forward-compat with
    /// future claude releases.
    #[serde(other, deserialize_with = "deserialize_ignored_subtype")]
    Other,
}

