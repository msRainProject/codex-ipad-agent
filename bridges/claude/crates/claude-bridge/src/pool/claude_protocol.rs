/// `{"type":"stream_event","event":{...},"session_id":..,"parent_tool_use_id":..,"uuid":..}`.
///
/// `event` is the Anthropic SDK `RawMessageStreamEvent` family — translation
/// happens in `claude_translate`. `parent_tool_use_id` carries the
