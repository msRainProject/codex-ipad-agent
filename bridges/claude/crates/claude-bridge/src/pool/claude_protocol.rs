// ============================================================================
// Outbound: claude → bridge on stdout
// ============================================================================

/// Demultiplexer for every line claude writes to stdout. Each line is exactly
/// one of these variants; the reader task in `process.rs` parses the line and
/// broadcasts a [`ClaudeEvent`] wrapping the result.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
#[serde(tag = "type", rename_all = "snake_case")]
pub enum ClaudeOutbound {
    System(SystemEvent),
    StreamEvent(StreamEventEnvelope),
    Assistant(AssistantEnvelope),
    User(UserEnvelope),
    RateLimitEvent(RateLimitEnvelope),
    Result(ResultEnvelope),
    /// Liveness ping from claude. Per the SDK protocol, MUST be silently
    /// ignored — failing to do so floods the reader's parse-error log.
    KeepAlive,
    /// claude → bridge in-band control RPC (e.g. `can_use_tool`,
    /// `hook_callback`, `mcp_message`). Bridge ignores in v1; permission
    /// bridging is a v2 follow-up (#13).
    ControlRequest(serde_json::Value),
    /// Reply to a bridge → claude `control_request`. The reader task peels
    /// `request_id` off and routes the payload to the matching pending-control
    /// oneshot in `pool::process` before broadcasting; the translator then
    /// silently drops the broadcast copy. Modeled as a typed envelope so the
    /// router doesn't reach into a `Value`.
    ControlResponse(ControlResponseEnvelope),
    /// Compact text summary stream. Not surfaced to codex in v1.
    StreamlinedText(serde_json::Value),
    /// Compact tool-use summary stream. Not surfaced to codex in v1.
    StreamlinedToolUseSummary(serde_json::Value),
}

// ----- control responses ---------------------------------------------------

/// `{"type":"control_response","response":{"request_id":"<uuid>","subtype":"success"|"error",...}}`.
///
/// **Wire-quirk:** `request_id` is nested INSIDE the outer `response` object
/// (the SDK looks it up at `Q.response.request_id`), NOT at top level. The
/// `response` field on `Success` carries the subtype-specific reply payload;
/// `Error` carries an `error` string. The bridge parses just enough to bucket
/// success vs. error and route by `request_id`; the inner payload is opaque
/// so new SDK reply shapes pass through without a schema bump.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct ControlResponseEnvelope {
    pub response: ControlResponseInner,
}

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
#[serde(tag = "subtype", rename_all = "snake_case")]
pub enum ControlResponseInner {
    Success {
        request_id: String,
        /// Subtype-specific reply payload. For `interrupt` claude sends
        /// `null`; for `rewind_files` this is the rewound state; for
        /// `can_use_tool` (when the bridge is acting as the tool-permission
        /// server, the SDK echoes back our `{behavior:..., ...}`). Opaque.
        #[serde(default, skip_serializing_if = "Option::is_none")]
        response: Option<Value>,
    },
    Error {
        request_id: String,
        error: String,
    },
}

impl ControlResponseInner {
    pub fn request_id(&self) -> &str {
        match self {
            Self::Success { request_id, .. } | Self::Error { request_id, .. } => request_id,
        }
    }
}

/// Surface the response/error shape back to `request_control`'s caller. The
/// outer pool-side enum we expose stays subtype-tagged for ergonomics; the
/// `request_id` field inside the wire envelope has already been peeled off
/// by the reader for routing.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
#[serde(tag = "subtype", rename_all = "snake_case")]
pub enum ControlResponseBody {
    Success {
        #[serde(default, skip_serializing_if = "Option::is_none")]
        response: Option<Value>,
    },
    Error {
        error: String,
    },
}

impl ControlResponseBody {
    pub fn from_inner(inner: ControlResponseInner) -> Self {
        match inner {
            ControlResponseInner::Success { response, .. } => Self::Success { response },
            ControlResponseInner::Error { error, .. } => Self::Error { error },
        }
    }
}

// ----- system ----------------------------------------------------------------

/// `{"type":"system","subtype":"init"|"status"|...,...}`.
///
/// Variants are tagged on `subtype`. The shape varies enough between subtypes
/// (init carries 30+ fields; status is just a string) that we model them
/// individually. Unknown subtypes degrade to [`SystemEvent::Other`] so we
/// never lose a line.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
#[serde(tag = "subtype", rename_all = "snake_case")]
pub enum SystemEvent {
    /// `SystemInit` is significantly larger than the other variants (~330B
    /// vs ~70B), so boxed to keep the enum cheap to clone — every
    /// translator subscriber receives a `ClaudeEvent` clone per stdout
    /// line.
    Init(Box<SystemInit>),
    Status(SystemStatus),
    /// Catch-all for future subtypes (e.g. claude could add `system/idle`,
    /// `system/upgrade_available`). Bridge logs and forwards the payload as
    /// opaque JSON.
    #[serde(other, deserialize_with = "deserialize_ignored_subtype")]
    Other,
}

fn deserialize_ignored_subtype<'de, D>(_d: D) -> Result<(), D::Error>
where
    D: serde::Deserializer<'de>,
{
    Ok(())
}

